package designpattern.builder.improve;

public class House {
    private String walls;
    private String roofs;
    private String bisics;

    public String getWalls() {
        return walls;
    }

    public void setWalls(String walls) {
        this.walls = walls;
    }

    public String getRoofs() {
        return roofs;
    }

    public void setRoofs(String roofs) {
        this.roofs = roofs;
    }

    public String getBisics() {
        return bisics;
    }

    public void setBisics(String bisics) {
        this.bisics = bisics;
    }
}
